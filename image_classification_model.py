    # Load + preprocess
    img = keras_image.load_img(filename, target_size=(150, 150))
    img_array = keras_image.img_to_array(img)
    img_array = np.expand_dims(img_array, axis=0) / 255.0

    # Predict
    pred = model.predict(img_array, verbose=0)[0][0]
    label = "Dog" if pred > 0.5 else "Cat"
    conf  = round(pred*100, 1) if pred > 0.5 else round((1-pred)*100, 1)

    # Display
    plt.figure(figsize=(5, 5))
    plt.imshow(PILImage.open(filename))
    plt.title(f'Prediction: {label}\nConfidence: {conf}%',
              fontsize=14, fontweight='bold',
              color='green' if conf > 85 else 'orange')
    plt.axis('off')
    plt.tight_layout()
    plt.show()

    print(f"\nResult: {label} — {conf}% confident!")
    print(f"Raw score: {pred:.4f} (>0.5=Dog, <0.5=Cat)")

